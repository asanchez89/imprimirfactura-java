package beans;

public class Comando {

	private String cmdPrimario;
	private String cmdDescuento;
	private Integer index;
	
	public String getCmdPrimario() {
		return cmdPrimario;
	}
	public void setCmdPrimario(String cmdPrimario) {
		this.cmdPrimario = cmdPrimario;
	}
	public String getCmdDescuento() {
		return cmdDescuento;
	}
	public void setCmdDescuento(String cmdDescuento) {
		this.cmdDescuento = cmdDescuento;
	}
	public Integer getIndex() {
		return index;
	}
	public void setIndex(Integer index) {
		this.index = index;
	}
	
	
}
